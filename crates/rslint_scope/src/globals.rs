//! Global variable definitions

use std::borrow::Cow;

pub struct JsGlobal {
    pub(crate) name: Cow<'static, str>,
    pub(crate) writeable: bool,
}

impl JsGlobal {
    pub fn new<N>(name: N, writeable: bool) -> Self
    where
        N: Into<Cow<'static, str>>,
    {
        Self {
            name: name.into(),
            writeable,
        }
    }

    pub const fn new_static(name: &'static str, writeable: bool) -> Self {
        Self {
            name: Cow::Borrowed(name),
            writeable,
        }
    }
}

